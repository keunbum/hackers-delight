int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);
  int n;
  cin >> n;
  vector<pair<int, int>> a(n);
  for (int i = 0; i < n; i++) {
    cin >> a[i].first >> a[i].second;
  }
  vector<long long> dp(n + 1, 0LL);
  for (int i = n - 1; i >= 0; i--) {
    auto[t, p] = a[i];
    dp[i] = dp[i + 1];
    if (i + t - 1 < n) {
      dp[i] = max(dp[i], dp[i + t] + p);
    }
  }
  cout << dp[0] << '\n';
  return 0;
}

