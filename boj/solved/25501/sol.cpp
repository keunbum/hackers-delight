int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);
  int tt;
  cin >> tt;
  while (tt--) {
    string s;
    cin >> s;
    int b = 0;
    int e = (int) s.size() - 1;
    while (b < e) {
      if (s[b] != s[e]) {
        break;
      }
      ++b;
      --e;
    }
    cout << (b >= e) << " " << (b + 1) << '\n';
  }
  return 0;
}

